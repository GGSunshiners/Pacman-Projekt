    private void startButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_startButtonMouseClicked
        this.settingsPanel.setVisible(false);
        this.gamePanel.setVisible(true);
        int pacmanSpeed=Integer.parseInt(this.pacmanSpeedTextField.getText());
        int corneringRate=Integer.parseInt(this.pacmanCorneringTextField.getText());
        int chaseSpeed=Integer.parseInt(this.chaseSpeedTextField.getText());
        int frightSpeed=Integer.parseInt(this.frightTextField.getText());
        int scatterSpeed=Integer.parseInt(this.scatterSpeedTextField.getText());
        int coc=Integer.parseInt(this.communicationTextField.getText());
        int viewDistance=Integer.parseInt(this.viewDistanceTextField.getText());
        int foodValue=Integer.parseInt(this.foodTextField.getText());
        int powerPelletValue=Integer.parseInt(this.powerPelletsTextField.getText());
        int ghostValue=Integer.parseInt(this.ghostTextField.getText());
        int minusPerDistance=Integer.parseInt(this.minusPerDistanceTextField.getText());
        int cherryValue=Integer.parseInt(this.cherryTextField.getText());
        int frightGhostValue=Integer.parseInt(this.frightGhostValueTextField.getText());
        Settings setting=new Settings(pacmanSpeed, corneringRate, chaseSpeed, frightSpeed, scatterSpeed, coc, viewDistance, foodValue, powerPelletValue, ghostValue, minusPerDistance, cherryValue, frightGhostValue);
        level=new Level(setting);
        if(gameEngine==null){
            gameEngine= new Engine(level, setting, this);
        } else {
            gameEngine.updateNewEngine(level, setting);
        }
        this.gamePanel.setFocusable(true);
    }//GEN-LAST:event_startButtonMouseClicked

