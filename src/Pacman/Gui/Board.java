    private void gamePanelKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_gamePanelKeyPressed
        int arrowKey = evt.getKeyCode();
        String direction;
        if (arrowKey == KeyEvent.VK_LEFT) {
            gameEngine.runMovingCondition("Left","Pacman");
        }
        else if (arrowKey == KeyEvent.VK_UP) {
            gameEngine.runMovingCondition("Up","Pacman");
        }
        else if (arrowKey == KeyEvent.VK_RIGHT) {
            gameEngine.runMovingCondition("Right","Pacman");
        }
        else if (arrowKey == KeyEvent.VK_DOWN) {
            gameEngine.runMovingCondition("Down","Pacman");
        }
    }//GEN-LAST:event_gamePanelKeyPressed

